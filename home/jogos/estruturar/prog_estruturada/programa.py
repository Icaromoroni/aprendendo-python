# usando a função print
print("\nMinha Primeira Aplicaçao Python\n")

# criando uma variável
mensagem = 'ola!'
numero = 2
pi = 3.14
print(mensagem)
print(numero)
print(pi)

# Metodos upper, lower capitalize

# metodo upper deixa todas as letras maiúsculas
print(mensagem.upper())

# metodo lower deixa todas as letra minúsculas
print(mensagem.lower())

# metodo capitalize faz a primeira letra da palavra ficar maiúscula
print(mensagem.capitalize())
